import java.util.Scanner;

public class day4_4 {
    public static void main(String[] args) {

        Scanner stdIn = new Scanner(System.in);

        System.out.print("How many star do you want ? : ");
        int ch = stdIn.nextInt();
        



        for(int i=1; i <= ch; i++){
            System.out.print("*");
        }




    }
}
